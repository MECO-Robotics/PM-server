type IteratedSeed<T extends { iteration: number; isArchived: boolean }> = Omit<
  T,
  "iteration" | "isArchived"
> &
  Partial<Pick<T, "iteration" | "isArchived">>;

type SeedSubsystem = IteratedSeed<Subsystem>;
type SeedMechanism = IteratedSeed<Mechanism>;
type SeedPartDefinition = IteratedSeed<PartDefinition>;
type SeedWorkstream = Omit<Workstream, "isArchived"> & Partial<Pick<Workstream, "isArchived">>;

type SeedTask = Omit<
  Task,
  | "workstreamIds"
  | "subsystemIds"
  | "mechanismIds"
  | "partInstanceIds"
  | "artifactId"
  | "artifactIds"
  | "assigneeIds"
> &
  Partial<
    Pick<
      Task,
      | "workstreamIds"
      | "subsystemIds"
      | "mechanismIds"
      | "partInstanceIds"
      | "artifactId"
      | "artifactIds"
      | "assigneeIds"
    >
  >;

function uniqueIds(values: Array<string | null | undefined>) {
  return Array.from(
    new Set(values.filter((value): value is string => Boolean(value))),
  );
}

function normalizeIteration(iteration: number | undefined) {
  return Number.isFinite(iteration) && iteration && iteration >= 1
    ? Math.trunc(iteration)
    : 1;
}

function withIteration<T extends { iteration?: number }>(
  item: T,
): Omit<T, "iteration"> & { iteration: number } {
  return {
    ...item,
    iteration: normalizeIteration(item.iteration),
  };
}

function withArchiveState<T extends { isArchived?: boolean }>(
  item: T,
): Omit<T, "isArchived"> & { isArchived: boolean } {
  return {
    ...item,
    isArchived: item.isArchived ?? false,
  };
}

function normalizeTaskTargets(task: SeedTask): Task {
  return {
    ...task,
    workstreamIds: task.workstreamIds ?? uniqueIds([task.workstreamId]),
    subsystemIds: task.subsystemIds ?? uniqueIds([task.subsystemId]),
    mechanismIds: task.mechanismIds ?? uniqueIds([task.mechanismId]),
    partInstanceIds: task.partInstanceIds ?? uniqueIds([task.partInstanceId]),
    artifactId: task.artifactId ?? null,
    artifactIds: task.artifactIds ?? uniqueIds([task.artifactId]),
    assigneeIds: task.assigneeIds ?? uniqueIds([task.ownerId]),
  };
}

